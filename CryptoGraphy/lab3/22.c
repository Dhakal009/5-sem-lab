// Helper to print binary
void print_bits(unsigned char *data, int nbits) {
	for (int i = 0; i < nbits; i++) {
		int byte = i / 8;
		int bit = 7 - (i % 8);
		printf("%d", (data[byte] >> bit) & 1);
		if ((i+1) % 8 == 0 && i != nbits-1) printf(" ");
	}
	printf("\n");
}


void print_binary_value(int value, int bits) {
	for (int i = bits - 1; i >= 0; i--) {
		printf("%d", (value >> i) & 1);
	}
}


void sbox(unsigned char *in, unsigned char *out) {
	printf("\nS-Box Processing (6-bit input -> 4-bit output):\n");
	for (int i = 0; i < 8; i++) {
		int offset = i * 6;
		int val = 0;
		
		for (int j = 0; j < 6; j++) {
			int b = ((in[(offset + j) / 8] >> (7 - ((offset + j) % 8))) & 1);
			val = (val << 1) | b;
		}
		int row = ((val & 0x20) >> 4) | (val & 0x01);
		int col = (val >> 1) & 0x0F;
		int s = S[i][row][col];
		
		int outbit = i * 4;
		for (int j = 0; j < 4; j++) {
			if (s & (1 << (3 - j))) {
				out[(outbit + j) / 8] |= (1 << (7 - ((outbit + j) % 8)));
			}
		}

		printf("S%d: ", i + 1);
		print_binary_value(val, 6);
		printf(" -> ");
		print_binary_value(s, 4);
		printf("\n");
	}
}

